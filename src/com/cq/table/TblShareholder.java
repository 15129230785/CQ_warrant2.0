package com.cq.table;

import java.io.Serializable;

public class TblShareholder implements Serializable {
	private static final long serialVersionUID = 1797463358256513563L;
	
	
	private int kid;
	private String eid;
	private String name;
	private Character type;
	private String sid;
	private double money;
	private Character mode;
	private double proportion;
	private String description;

	public TblShareholder() {
	}

	public TblShareholder(int kid, String eid,  String name,
			Character type, String sid, double money, Character mode,
			double proportion) {
		super();
		this.kid = kid;
		this.eid = eid;
		this.name = name;
		this.type = type;
		this.sid = sid;
		this.money = money;
		this.mode = mode;
		this.proportion = proportion;
	}
	
	public int getKid() {
		return kid;
	}

	public void setKid(int kid) {
		this.kid = kid;
	}

	public String getEid() {
		return eid;
	}

	public void setEid(String eid) {
		this.eid = eid;
	}

	public String getName() {
		return this.name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Character getType() {
		return type;
	}

	public void setType(Character type) {
		this.type = type;
	}

	public Character getMode() {
		return mode;
	}

	public void setMode(Character mode) {
		this.mode = mode;
	}

	public String getSid() {
		return this.sid;
	}

	public void setSid(String sid) {
		this.sid = sid;
	}

	public double getMoney() {
		return this.money;
	}

	public void setMoney(double money) {
		this.money = money;
	}

	public double getProportion() {
		return this.proportion;
	}

	public void setProportion(double proportion) {
		this.proportion = proportion;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}
}
