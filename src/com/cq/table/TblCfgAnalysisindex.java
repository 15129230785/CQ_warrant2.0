package com.cq.table;

import java.io.Serializable;

public class TblCfgAnalysisindex implements Serializable {
	private static final long serialVersionUID = 7832989449425568483L;
	
	private int kid;
	private String name;
	private String cname;
	private double ratio;

	public TblCfgAnalysisindex() {
	}

	public TblCfgAnalysisindex(int kid, String name, String cname, double ratio) {
		this.kid = kid;
		this.name = name;
		this.cname = cname;
		this.ratio = ratio;
	}

	public int getKid() {
		return kid;
	}

	public void setKid(int kid) {
		this.kid = kid;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCname() {
		return cname;
	}

	public void setCname(String cname) {
		this.cname = cname;
	}

	public double getRatio() {
		return ratio;
	}

	public void setRatio(double ratio) {
		this.ratio = ratio;
	}
}
