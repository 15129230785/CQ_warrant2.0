package com.cq.table;

import java.io.Serializable;
import java.util.Date;

public class TblChangeinfo implements Serializable {
	private static final long serialVersionUID = 7224085433332653481L;
	
	private int kid;
	private String rid;
	private Date chgDate;
	private Character chgType;
	private String description;

	public TblChangeinfo() {
	}

	public int getKid() {
		return kid;
	}

	public void setKid(int kid) {
		this.kid = kid;
	}

	public String getRid() {
		return rid;
	}

	public void setRid(String rid) {
		this.rid = rid;
	}

	public Date getChgDate() {
		return chgDate;
	}

	public void setChgDate(Date chgDate) {
		this.chgDate = chgDate;
	}

	public Character getChgType() {
		return chgType;
	}

	public void setChgType(Character chgType) {
		this.chgType = chgType;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}
}
