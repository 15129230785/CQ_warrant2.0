package com.cq.table;

import java.io.Serializable;
import java.util.Date;

public class TblDecision implements Serializable {
	private static final long serialVersionUID = -1205710227085315623L;
	
	private int kid;
	private String wid;
	private Date date;
	private Character result;
	private String explains;

	public TblDecision() {
		
	}

	public TblDecision(int kid, String wid, Date date, Character result, String explain) {
		super();
		this.kid = kid;
		this.wid = wid;
		this.date = date;
		this.result = result;
		this.explains = explain;
	}

	public int getKid() {
		return kid;
	}

	public void setKid(int kid) {
		this.kid = kid;
	}

	public String getWid() {
		return wid;
	}

	public void setWid(String wid) {
		this.wid = wid;
	}
	public String getExplains() {
		return explains;
	}

	public void setExplains(String explains) {
		this.explains = explains;
	}

	public Date getDate() {
		return date;
	}

	public void setDate(Date date) {
		this.date = date;
	}

	public Character getResult() {
		return result;
	}

	public void setResult(Character result) {
		this.result = result;
	}
}
