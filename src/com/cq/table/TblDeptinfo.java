package com.cq.table;

import java.io.Serializable;

public class TblDeptinfo implements Serializable {
	private static final long serialVersionUID = 8850850806075590586L;
	
	private int kid;
    private String eid;
	private String name;
	private String duty;

	public TblDeptinfo() {
	}
	
	public TblDeptinfo(String eid, String name, String duty) {
		super();
		this.eid = eid;
		this.name = name;
		this.duty = duty;
	}

	public int getKid() {
		return kid;
	}

	public void setKid(int kid) {
		this.kid = kid;
	}

	public String getEid() {
		return eid;
	}
	
	public void setEid(String eid) {
		this.eid = eid;
	}
	
	public String getName() {
		return this.name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDuty() {
		return this.duty;
	}

	public void setDuty(String duty) {
		this.duty = duty;
	}

}
